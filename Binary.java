import java.lang.*;
import java.util.*;

public class Binary {
    public static void main(String[] args) {
        int a = 10, b = 6;
        int z = a & b;
        int x = a | b;
        int y = ~a;
        int u = a ^ b;

        System.out.println(z);
        System.out.println(x);
        System.out.println(y);
        System.out.println(u);


    }
}

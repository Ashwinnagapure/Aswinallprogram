import java.lang.*;
import java.util.*;

class walk {
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        int t;
        t = s.nextInt();
        while (t-- > 0) {
            int x;
            x = s.nextInt();
            int y = x * 10;
            System.out.println(y);
        }
    }
}
